#Python array
#create an array
data_type=["string","list","tuple","dict"]
print(data_type)
#Acess the element of an array
x=data_type[1]
print(x)
#Length of an array
x=len(data_type)
print(x)
#looping array elements
for x in data_type:
    print(x)
#Adding array elements
data_type.append("set")
print(data_type)
#Remove Array elements
data_type.remove("set")
print(data_type)
